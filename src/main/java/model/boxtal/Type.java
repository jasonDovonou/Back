package model.boxtal;

public class Type {
	private String code;
	private String label;

	// Getter Methods

	public String getCode() {
		return code;
	}

	public String getLabel() {
		return label;
	}

	// Setter Methods

	public void setCode(String code) {
		this.code = code;
	}

	public void setLabel(String label) {
		this.label = label;
	}
}
